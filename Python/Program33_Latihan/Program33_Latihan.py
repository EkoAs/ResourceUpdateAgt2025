# pengulanagan / looping
for i in range(1, 20, 2): # range dipakai untuk angka misal satu sampai sepuluh 
    # angka 2 untuk langkah, jadi 1, 3, 5, 7, 9
    # range(start, stop, step)
    print(f"{i:<5} Hello World") # <5 artinya adalah 5 karakter, jadi jika i = 1 maka akan ada 4 spasi sebelum Hello World
