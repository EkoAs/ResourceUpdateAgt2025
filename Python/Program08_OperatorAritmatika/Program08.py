import math
 # operator aritmatika
a = 10
b = 3

# operasi penambaham
hasil = a + b 
print(a, '+', b , '=', hasil) #hasil = 13

# operasi pengurangan
hasil = a - b 
print(a, '-', b , '=', hasil) #hasil = 7

# operasi perkarkalian
hasil = a * b 
print(a, '*', b , '=', hasil) #hasil = 30

# operasi pembagian 
hasil = a / b
print(a, '/', b , '=', hasil) #hasil = 3.3333333333333335

# operasi pangkat
hasil = a ** b
print(a, '**', b , '=', hasil) #hasil = 1000

# operasi modulus
hasil = a % b 
print(a, '%', b , '=', hasil) #hasil = 1

# operasi floor division 
hasil = a // b 
print(a, '//', b , '=', hasil) #hasil = 3